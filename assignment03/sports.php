	<h2>New York Yankees</h2>


	<p>The New York Yankees are an American professional baseball team based in the New York City borough of the Bronx. The Yankees compete in Major League Baseball (MLB) as a member club of the American League (AL) East division. They are one of two major league clubs based in New York City, the other being the New York Mets of the National League. In the 1901 season, the club began play in the AL as the Baltimore Orioles (no relation to the modern Baltimore Orioles). Frank Farrell and Bill Devery purchased the franchise (which had ceased operations) and moved it to New York City, renaming the club the New York Highlanders.The Highlanders were officially renamed the Yankees in 1913.</p>


	<h3>History</h3>

	<p>At the end of 1900, Ban Johnson, president of the American League—a minor league previously known as the Western League (1894–1899)—reorganized the league. He added teams in three East Coast cities, forming the American League (AL) as a major league in an attempt to challenge the National League (NL) for supremacy. Plans to add a team in New York City were blocked by the NL's New York Giants, who had enough political power in New York City to prevent the AL from establishing a team. Instead, a team was placed in Baltimore, Maryland, a city which the NL abandoned when it contracted from 12 to 8 teams in 1900.</p>

