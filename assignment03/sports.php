	<h2>New England Patriots</h2>

	<p>The New England Patriots are a professional American football team based in the Greater Boston region. The Patriots compete in the National Football League (NFL) as a member club of the league's American Football Conference (AFC) East division. The team plays its home games at Gillette Stadium in the town of Foxborough, Massachusetts, which is located 21 miles (34 km) southwest of downtown Boston, Massachusetts and 20 miles (32 km) northeast of downtown Providence, Rhode Island. The Patriots are also headquartered at Gillette Stadium. </p>

	<h3>History</h3>

	<p>On November 16, 1959, Boston business executive Billy Sullivan was awarded the eighth and final franchise of the developing American Football League (AFL). The following winter, locals were allowed to submit ideas for the Boston football team's official name. The most popular choice – and the one that Sullivan selected – was the "Boston Patriots," with "Patriots" referring to those colonists of the Thirteen Colonies who rebelled against British control during the American Revolution and in July 1776 declared the United States of America an independent nation. Immediately thereafter, artist Phil Bissell of The Boston Globe developed the "Pat Patriot" logo.</p>

